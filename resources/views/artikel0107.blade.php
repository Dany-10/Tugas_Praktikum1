<h2>PENGERTIAN</h2>
<h5>Bahasa pemrograman, atau sering diistilahkan juga dengan bahasa komputer 
atau bahasa pemrograman komputer, adalah instruksi standar untuk memerintah komputer. 
Bahasa pemrograman ini merupakan suatu himpunan dari aturan sintaks dan semantik 
yang dipakai untuk mendefinisikan program komputer. Bahasa ini memungkinkan seorang 
programmer dapat menentukan secara persis data mana yang akan diolah oleh komputer, 
bagaimana data ini akan disimpan/diteruskan, 
dan jenis langkah apa yang akan diambil dalam berbagai situasi secara persis.</h5>
<br/>
<h5>Menurut tingkat kedekatannya dengan mesin komputer, bahasa pemrograman terdiri dari:</h5>
<li>1.	Bahasa Mesin, yaitu memberikan perintah kepada komputer dengan memakai kode bahasa biner, contohnya 01100101100110</li>
<li>2.	Bahasa Tingkat Rendah, atau dikenal dengan istilah bahasa rakitan (bah.Inggris Assembly), yaitu memberikan perintah kepada komputer dengan memakai kode-kode singkat (kode mnemonic), contohnya kode_mesin|MOV, SUB, CMP, JMP, JGE, JL, LOOP, dsb.</li>
<li>3.	Bahasa Tingkat Menengah, yaitu bahasa komputer yang memakai campuran instruksi dalam kata-kata bahasa manusia[butuh rujukan] (lihat contoh Bahasa Tingkat Tinggi di bawah) dan instruksi yang bersifat simbolik, contohnya {, }, ?, <<, >>, &&, ||, dsb.</li>
<li>4.	Bahasa Tingkat Tinggi, yaitu bahasa komputer yang memakai instruksi berasal dari unsur kata-kata bahasa manusia, contohnya begin, end, if, for, while, and, or, dsb.[butuh rujukan] Komputer dapat mengerti bahasa manusia itu diperlukan program compiler atau interpreter.</li>
<h5>Sebagian besar bahasa pemrograman digolongkan sebagai Bahasa Tingkat Tinggi, hanya bahasa C yang digolongkan sebagai Bahasa Tingkat Menengah dan Assembly yang merupakan Bahasa Tingkat Rendah.</h5>
<br/>

<h2>FUNGSI</h2>
<li>Fungsi bahasa pemrograman yaitu memerintah komputer untuk mengolah data sesuai dengan alur berpikir yang kita inginkan. Keluaran dari bahasa pemrograman tersebut berupa program/aplikasi. Contohnya adalah program yang digunakan oleh kasir di mal-mal atau swalayan, penggunaan lampu lalu lintas di jalan raya, dll.
Bahasa Pemrograman yang kita kenal ada banyak sekali di belahan dunia, tentang ilmu komputer dan teknologi dewasa ini. Perkembangannya mengikuti tingginya inovasi yang dilakukan dalam dunia teknologi. Contoh bahasa pemrograman yang kita kenal antara lain adalah untuk membuat aplikasi game, antivirus, web, dan teknologi lainnya.
Bahasa pemrograman komputer yang kita kenal antara lain adalah Java, Visual Basic, C++, C, Cobol, PHP, .Net, dan ratusan bahasa lainnya. Namun tentu saja kebutuhan bahasa ini harus disesuaikan dengan fungsi dan perangkat yang menggunakannya.
Secara umum bahasa pemrograman terbagi menjadi 4 kelompok, yaitu:
Object Oriented Language (Visual dBase, Visual FoxPro, Delphi, Visual C)
High Level Language (seperti Pascal dan Basic)
Middle Level Language (seperti bahasa C), dan
Low Level Language (seperti bahasa Assembly)
</li>
